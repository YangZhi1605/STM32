//4、编写中断服务函数，为了方便管理，和其他中断服务函数放在一起了
//到启动文件中找官方定好的名字，不然启动文件识别不了我们写的内容

void EXTI0_IRQHandler(void)
{
	//中断流程差不多的：
	//确保是要操作的中断线,确保真的进入中断了
	//EXTI_GetITStatus(uint32_t EXTI_Line);
	
	/*
	EXTI_GetITStatus函数用来获取EXTI的中断标志位状态，
	如果EXTI线有中断发生函数返回“SET”
	否则返回“RESET”。 
	实际上，EXTI_GetITStatus函数是通过读取EXTI_PR寄存器值来判断EXTI线状态的。
	*/
	if(EXTI_GetITStatus(EXTI_Line0) != RESET)
	{
		//写中断具体要干的事儿
		//这里写的让LED_G翻转一次
		LEDG_TOGGLE;
	}
	
	//执行完之后，将中断标志物清除掉
	EXTI_ClearITPendingBit(EXTI_Line0);
}

